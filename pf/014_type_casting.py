"""
Type casting refers to changing the data type of a variable
"""

var_num_int = 2

#   convert int to float by using float function around int value
var_num_float = float(var_num_int)

#   convert int to str by using str function around int value
var_num_string = str(var_num_int)


print(f'int variable: {var_num_int}, Type: {type(var_num_int)}')
print(f'float variable: {var_num_float}, Type: {type(var_num_float)}')
print(f'string variable: {var_num_string}, Type: {type(var_num_string)}')